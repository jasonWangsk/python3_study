# 运算符

num1 = 100
num2 = 15
print(num1/num2)
print(round(num1/num2,2)) # 保留小数点后几位
print(num1//num2) # 整除
print(num1%num2) # 求 余数
print(num1**3) # **开方 （2 平方，3 立方。。。）
print(num1**0.5) # **0.5 求平方根

